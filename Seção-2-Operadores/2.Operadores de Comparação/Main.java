public class Main {
	public static void main(String[] args) {

		/*
			Igual a (==)
			Diferente de (!=)
			Maior que (>)
			Menor que (<)
			Maior ou igual a (>=)
			Menor ou igual a (<=)
		*/

		int a = 10;
		int b = 20;

		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
	}
}