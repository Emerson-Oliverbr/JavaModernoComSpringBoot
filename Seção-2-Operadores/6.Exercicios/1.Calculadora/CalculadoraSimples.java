public class CalculadoraSimples {
	public static void main(String[] args) {

		int a = 10;
		int b = 2;

		System.out.println("O resultado de: " + a + " + " + b + " = " + (a + b));
		System.out.println("O resultado de: " + a + " - " + b + " = " + (a - b));
		System.out.println("O resultado de: " + a + " * " + b + " = " + (a * b));
		System.out.println("O resultado de: " + a + " / " + b + " = " + (a / b));
		System.out.println("O resultado de: " + a + " % " + b + " = " + (a % b));
	}
}