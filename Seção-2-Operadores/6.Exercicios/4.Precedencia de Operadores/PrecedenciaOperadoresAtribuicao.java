public class PrecedenciaOperadoresAtribuicao {
	public static void main(String[] args) {

		int numero = 5;
		int numero2 = 5;
		int numero3 = 5;
		int numero4 = 5;

		++numero;
		System.out.println(numero);
		numero2++;
		System.out.println(numero2);
		--numero3;
		System.out.println(numero3);
		numero4--;
		System.out.println(numero4);
	}
}