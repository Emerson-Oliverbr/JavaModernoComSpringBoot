public class Main {
	public static void main(String[] args) {

		/*
			Adição (+)
			Subtracao(-)
			Multiplicacao(*)
			Divisao(/)
			Modulo(%)
		*/

		int a = 10;
		int b = 2;

		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
	}
}