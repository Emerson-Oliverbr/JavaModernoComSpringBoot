public class Main {
	public static void main(String[] args) {

	/*
		OPERADORES UNÁRIOS	
		Incremento(++)
		Decremento(--)
		Positivo(+)
		Negativo(-)
		Negação lógica(!)
	*/

		int a = 10;
		int b = 10;

		a++; //Equivale à a = a + 1	
		b--; //Equivale à b = b - 1	

		System.out.println("Valor de a = " + a);
		System.out.println("Valor de b = " + b);
	}
}